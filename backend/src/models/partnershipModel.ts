import mongoose from "mongoose";

const partnershipSchema = new mongoose.Schema({
  websiteUrl: {
    type: String,
    required: true,
  },
  image: {
    type: String,
    required: true,
  },
  email: {
    type: String,
    required: true,
  },
  createdAt: {
    type: Date,
    default: Date.now,
  },
});

export default mongoose.model("Partnership", partnershipSchema);